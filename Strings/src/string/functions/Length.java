package string.functions;

public class Length {
	
	public static void main(String[] args) {
		
		String s1 = "GreenTechnology";
		String s2 = "SeleniumAutomationtool";
		String s3 = "velmurugan";
		String s4 = "javaprogram";
		String s5 = "9095484678";
		
		System.out.println(s1.length());
		System.out.println(s2.length());
		System.out.println(s3.length());
		System.out.println(s4.length());
		System.out.println(s5.length());
		System.out.println(s1.indexOf('o'));
		System.out.println(s2.indexOf('o'));
		System.out.println(s3.indexOf('n'));
		System.out.println(s5.indexOf('8'));
		String s = " j a v a p r o g r a m ";
		System.out.println(s.lastIndexOf(" "));
		System.out.println(s1.charAt(s1.indexOf('h')));
		System.out.println(s2.charAt(s2.indexOf('o')));
		System.out.println(s3.charAt(s3.indexOf('u')));
		System.out.println(s4.charAt(s4.indexOf('p')));
		System.out.println(s5.charAt(s5.indexOf('7')));
		
		
		
	}

	
	
	
	}

	


