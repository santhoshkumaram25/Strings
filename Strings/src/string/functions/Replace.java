package string.functions;

public class Replace {
	public static void main(String[] args) {
		String a = "Welcome to class java";
		System.out.println(a.replace("java", "sql"));
		String b = "Greens Adayar";
		System.out.println(b.replace("Adayar","Omr"));
		String c = "Welcome to java class";
		System.out.println(c.replace(" ", "#"));
	}

}
